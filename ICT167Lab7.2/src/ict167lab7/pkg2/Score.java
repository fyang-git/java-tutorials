package ict167lab7.pkg2;

public class Score {
    private String name;
    private int score;

    // Default constructor
    public Score() {
    }

    // Getters and Setters
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getScore() {
        return score;
    }

    public void setScore(int score) {
        this.score = score;
    }
}